// ---Task-11-----------------------------------------------------------------------------
const complexOperation = (
  a: number,
  b: number,
  c: number,
  d: number,
  e: number,
  f: number
): number => {
  const sum = a + b;
  const subtracted = sum - c;
  const multiplied = subtracted * d;
  const divided = multiplied / e;
  const result = divided ** f;
  return result;
};

console.log(complexOperation(6, 2, 1, 4, 2, 3));

